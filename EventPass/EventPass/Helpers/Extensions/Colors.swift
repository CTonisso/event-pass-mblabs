import Foundation
import UIKit

extension UIColor {
    
    @nonobjc class var baseBlue: UIColor {
        return UIColor(red: 0.0 / 255.0, green: 97.0 / 255.0, blue: 198.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var highlightYellow: UIColor {
        return UIColor(red: 241.0 / 255.0, green: 196.0 / 255.0, blue: 15.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var textGrey: UIColor {
        return UIColor(red: 155.0 / 255.0, green: 155.0 / 255.0, blue: 155.0 / 255.0, alpha: 1.0)
    }
}
